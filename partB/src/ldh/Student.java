package ldh;


    
// 학생 클래스 정의
class Student {
    private String name;
    private int age;

    // 생성자
    public Student(String name, int age) {
        this.name = name;
        this.age = age;
    }

    // Getter 메소드
    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }
}


